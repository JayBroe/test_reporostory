def add(a, b):
    """Zwraca sumę dwóch liczb."""
    return a + b


def subtract(a, b):
    """Zwraca różnicę dwóch liczb."""
    return a - b

def multiply(a, b):
    """Zwraca wynik mnożenia dwóch liczb"""
    return a * b

if __name__ == "__main__":
    print("Witaj w mojej aplikacji Python!")
    print("2 + 3 =", add(2, 3))

    print("5 - 1 =", subtract(5, 1))

    print("2 * 2 =", multiply(2,2))

